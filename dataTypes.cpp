#include <iostream>
#include <string>
using namespace std;
 
int main () {
  int a = 25;               
  float b = 7.50;     
  double c = 10.25;   
  char d = 'M';         
  bool e = true;       
  string f = "My name is";   
   
  // To print the values of the variables
  cout << "int: " << a << "\n";
  cout << "float: " << b << "\n";
  cout << "double: " << c << "\n";
  cout << "char: " << d << "\n";
  cout << "bool: " << e << "\n";
  cout << "string: " << f << "\n";
 
  return 0;
}
